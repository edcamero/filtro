    private void tablaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaMouseClicked
        if (evt.getButton() == 3) {
            JTable source = (JTable) evt.getSource();
            int row = source.rowAtPoint(evt.getPoint());
            int seleccion = JOptionPane.showOptionDialog(
                    this,
                    "Seleccione opcion",
                    "Selector de opciones",
                    JOptionPane.YES_NO_CANCEL_OPTION,
                    JOptionPane.QUESTION_MESSAGE,
                    null, // null para icono por defecto.
                    new Object[]{"Editar", "Eliminar", "Cancelar"}, // null para YES, NO y CANCEL
                    "opcion 1");
            //int column = source.columnAtPoint( evt.getPoint() );
            String id_s = "" + source.getModel().getValueAt(row, 0);

            int id = Integer.parseInt(id_s);
            opcionesBujia(id, seleccion);
        }
// TODO add your handling code here:
    }//GEN-LAST:event_tablaMouseClicked

    private void opcionesBujia(int id, int opc) {
        switch (opc) {
            case 0:
                   BujiaController.getInstancia().editarBujiaGui(id);
                break;
            case 1:
                Bujia bujia=BujiaController.getInstancia().buscar(id);
                String respuesta = JOptionPane.showInputDialog("Escribe " + bujia.getNombre() + " para confirmar que desea eliminar la bujia");
                if ( bujia.getNombre().equalsIgnoreCase(respuesta)) {
                    if ( BujiaController.getInstancia().eliminarBujia(id) ) {
                         JOptionPane.showMessageDialog(rootPane, "se eliminno la bujia");
                    }else{
                        JOptionPane.showMessageDialog(this, "No se eliminno el Purificador ya esta asociado a un cliente",
                                "Mensaje de Error",JOptionPane.ERROR_MESSAGE);
                    }

                }else{
                        JOptionPane.showMessageDialog(this, "Error a confirmar para eliminar bujia",
                                "Mensaje de Error",JOptionPane.ERROR_MESSAGE);
                    }

                break;

        }

    }

