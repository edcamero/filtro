    private boolean validar() {

        if (textNombre.getText().isEmpty() ||textPassword.getText().isEmpty() || textPasswordConfirm.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Llene todos los campos", "Mensaje de Error", JOptionPane.ERROR_MESSAGE);
            return false;
        } else {
            if (!textPassword.getText().equals(textPasswordConfirm.getText())) {
                JOptionPane.showMessageDialog(this, "las contrase√±as deben coincidir", "Mensaje de Error", JOptionPane.ERROR_MESSAGE);
                return false;
            }

        }
        return true;

    }

