    private void limpiar() {
        comboMaterial.setSelectedIndex(0);
        textModelo.setText("");
        textNombre.setText("");
        textPrecio.setText("");

    }

    private void MostrarDatos() {
        comboMaterial.setSelectedItem(this.myEquipo.getMaterial());
        textModelo.setText(this.myEquipo.getModelo());
        textNombre.setText(this.myEquipo.getNombre());
        textPrecio.setText(String.valueOf(this.myEquipo.getPrecio()));
        if (this.myEquipo.getDisponible()) {
            comboDispo.setSelectedIndex(0);
        } else {
            comboDispo.setSelectedIndex(1);
        }
    }

    public void keyTyped(KeyEvent e) {
        //se agrega funcion que obliga las letras en el textbox a ser mayusculas 
        char c = e.getKeyChar();
        //"e" es el nombre asignado al evento de tecla "c" representa a las letras
        if (Character.isLowerCase(c)) {
            String cad = ("" + c).toUpperCase();
            c = cad.charAt(0);
            e.setKeyChar(c);
        }

    }
