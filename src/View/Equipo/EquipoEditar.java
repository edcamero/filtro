    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // equipoControl.agregar();
        int resp = 0;
        myEquipo.setMaterial((String) this.getComboMaterial().getSelectedItem());

        myEquipo.setModelo(this.getTextModelo().getText());
        myEquipo.setNombre(this.getTextNombre().getText());
        myEquipo.setPrecio(Integer.parseInt(this.getTextPrecio().getText()));
        boolean dispo = this.getComboDispo().getSelectedIndex() == 0;
        myEquipo.setDisponible(dispo);
        if (EquipoController.getInstancia().editar(myEquipo)) {
            JOptionPane.showMessageDialog(null, "Se han registrado los cambios correctamente.");
            this.cerrar();
        } else {
            JOptionPane.showMessageDialog(this, "error a editar equipo", "Mensaje de error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

